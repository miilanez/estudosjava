package exercicios.comandos.de.repeticao;

public class repeticaoMensagem {

	/* 1) Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar
	Algoritmos!”. */
	
	public static void main(String[] args) {
			
		for(int frase =1; frase <= 20; frase ++) {
		
		System.out.println("Repetição " + frase + ": Eu gosto de estudar algorítmos!");
		
		}
	}

}
